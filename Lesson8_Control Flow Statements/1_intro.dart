Control Flow Statements are essential constructs in programming that allow you to control the flow of execution 
in your code. They enable you to make decisions, perform repetitive tasks, and handle different scenarios based on
 certain conditions. In Dart, you have several control flow statements that help you achieve these functionalities.


 The main control flow statements in Dart are:

  If-Else Statements:

    The if statement allows you to execute a block of code conditionally based on a Boolean expression.
    The if-else statement extends the if statement by providing an alternative block of code to execute when the condition
     is not met.
  
  Switch Statements:

    The switch statement allows you to execute different blocks of code based on the value of a variable or an expression.
    It provides an alternative to multiple if-else statements and offers more concise code for handling multiple cases.
  
  For Loop:

    The for loop allows you to execute a block of code repeatedly for a specific number of iterations.
    It consists of an initialization statement, a condition for iteration, an expression for incrementing or decrementing,
     and the loop body.
  
  While and Do-While Loops:

    The while loop executes a block of code repeatedly as long as a condition remains true.
   The do-while loop is similar to the while loop but guarantees that the loop body is executed at least once before checking the condition.
 






