void main(){ 

    // Unary Operators

    //Unary operators are operators that perform operations on a single operand, which can be either a variable, a constant, or an expression. 

  // Increment Operators ++

  // 1. Postfix Increment Operator

  int a =10;
   a++;
    print(a);  


  // 2. Prefix Increment Operator
  int b=5;
   ++b;
    print(b);  


 // Decrement Operators --


  // 1. Postfix Decrement Operator

  int c =10;
   c--;
    print(c);  


  // 2. Prefix Decrement Operator
  int d=5;
   --d;
    print(d);  


}