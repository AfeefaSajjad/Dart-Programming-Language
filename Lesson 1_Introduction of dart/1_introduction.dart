
/* 

* What is programming?

    Programming refers to a technological process for telling a computer which tasks to perform in order to solve problems.



* How does computer programming work?

    In short, programming follows a few steps: 

First, a programmer writes code a set of letters, numbers, and other characters. 

Next, a compiler converts each line of code into a language a computer can understand. 

Then, the computer scans the code and executes it, thereby performing a task or series of tasks. 



* What is Dart Programming?

  Dart is an open-source, general-purpose, object-oriented programming language with C-style syntax developed by Google in 2011. The purpose of Dart programming is to create a frontend user interfaces for the web and mobile apps. 

   It supports most of the common concepts of programming languages like classes, interfaces, functions, unlike other programming languages. Dart language does not support arrays directly.

